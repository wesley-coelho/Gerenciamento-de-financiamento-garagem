    private void btnSalvarEntradaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarEntradaActionPerformed
        // TODO add your handling code here: 
        
        String nomeMunicipio = "";
        String nome_Uf = "";
        
        int id_Uf = 0;
        EntradaVeiculo entradaVeiculo = new EntradaVeiculo();
        
        List<Estado> estados = EstadoDB.searchByName(nome_Uf);
                id_Uf = estados.getFirst().getId();              
                String sql = "SELECT * FROM municipio WHERE nome = '"+nomeMunicipio+"' AND id_uf = "+id_Uf+";";
                 Connection conn = ConnectionFactory.getConexao();         
                try {            
                    Statement stmt = conn.createStatement();
                    ResultSet resultSet = stmt.executeQuery(sql);            
                    while(resultSet.next()){               
                       entradaVeiculo.setId_municipio(resultSet.getInt("id"));
                       
                        }
                    ConnectionFactory.close(conn, stmt, resultSet);          
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(this, "Erro: \nEstado e cidade não correspondem. \nOperação cancelada!");
                    Logger.getLogger(Usuario.class.getName()).log(Level.SEVERE, null, ex);
                }              
     
        
        entradaVeiculo.setUsuario(this.usuario.getUsuario());       
        JOptionPane.showMessageDialog(this,entradaVeiculo.getData_entrada());
         JOptionPane.showMessageDialog(this,entradaVeiculo.getId_municipio());
        EntradaVeiculoDB.save(entradaVeiculo);
        JOptionPane.showMessageDialog(this, "Nova entrada de veículo cadastrada com sucesso!");
        
        
        
        
    }//GEN-LAST:event_btnSalvarEntradaActionPerformed

